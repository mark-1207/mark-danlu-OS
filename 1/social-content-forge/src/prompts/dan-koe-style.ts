export const DAN_KOE_STYLE_GUIDE = `
## Dan Koe Style Core Elements

### Voice & Tone
- Conversational, direct address to reader ("you")
- Personal anecdotes mixed with theory
- Challenging conventional wisdom
- Confident but not arrogant

### Structure
- **Opening (10%)**: Provocative statement or counter-intuitive viewpoint
  - Hook the reader immediately
  - State what they'll gain
  - List main points (e.g., "7 ideas")

- **Body (80%)**: Numbered sections in Chinese
  - Each section: provocative title + theory + case study + counter-question
  - Progressive logic: diagnose → reveal → solve

- **Closing (10%)**: Brief summary or encouragement
  - Personal sign-off

### Language
- Mix of colloquial and academic
- Rhythm: long explanations + short impact sentences
- Repeat key concepts from different angles
- Short sentences to emphasize

### Rhetoric
- Heavy use of contrast (past vs present, traditional vs innovation)
- Vivid analogies
- Parallel sentence structures
- Questions to provoke self-reflection

### Content Strategy
- Provide frameworks and models
- Value-first, then naturally mention products/services
- Acknowledge not everyone will benefit
- Create "you're special" belonging

### Writing Instructions
1. Find counter-intuitive angle or overlooked truth
2. Design 5-7 progressive sections
3. Reference authorities/research in theory sections
4. Provide actionable steps in practice sections
5. Use personal examples ("my" experience)
6. Insert short sentences/questions every 2-3 paragraphs
7. Target 5000-8000 Chinese characters for long-form

### Taboos
- Empty motivational quotes
- Over-promising ("guaranteed", "definitely")
- Outdated marketing language
- Ignoring complexity and contradictions
`;

