黄底黑字：醒目、冲击力
白底黑字：清晰、正式
黑底白字：高级、神秘
红底白字：紧急、警示

【动画效果】
打字机效果：适合金句逐字出现
弹跳效果：适合轻松搞笑内容
渐显效果：适合温情治愈内容
放大效果：适合强调重点

【排版原则】
一行不超过12个字
重点词语单独一行
配合说话节奏出现
重要信息停留3秒以上
